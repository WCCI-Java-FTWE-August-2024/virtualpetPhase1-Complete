package wcci.org.virtualpet.Enums;

/**
 * The DeathBy enum represents different causes of death for pets.
 */
public enum DeathBy {
    /**
     * Represents no cause of death.
     */
    None,

    /**
     * Represents death by loneliness.
     */
    Loneliness,

    /**
     * Represents death by starvation.
     */
    Starvation,

    /**
     * Represents death by disease.
     */
    Disease,
    /**
     * Represents death by Thirst.
     */
    Thirst,
    /**
     * Robotic Pets with no oil
     */
    NoOil,
    /**
     * Robotic pets no power
     */
    NoPower
}
