package wcci.org.virtualpet.Enums;
/**
 * The PetType enum represents different types of pets.
 */
public enum PetType {
    /**
     * Represents a cat.
     */
    CAT,

    /**
     * Represents a dog.
     */
    DOG,

     /**
     * Represents a robotic cat.
     */
    ROBOTIC_CAT,

    /**
     * Represents a dog.
     */
    ROBOTIC_DOG
}

